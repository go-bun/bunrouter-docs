<template>
  <div>
    <div class="system-bar system-bar--fixed">
      <div class="spacer"></div>

      <div v-if="link" class="d-none d-sm-block">
        <span class="emoji">&#9889;</span>
        <a :href="link.href" :target="link.href.startsWith('/') ? '_self' : '_blank'">{{
          link.text
        }}</a>
      </div>

      <div class="spacer"></div>

      <div class="links">
        <a href="https://github.com/uptrace/uptrace" target="_blank">Tracing & Metrics</a>
        <a href="https://bun.uptrace.dev/" target="_blank">SQL client</a>
        <a href="https://blog.uptrace.dev/" target="_blank">Blog</a>
      </div>
    </div>
    <Layout>
      <template #page-bottom>
        <div
          class="theme-default-content d-flex align-center justify-space-between"
          style="padding-top: 40px; padding-bottom: 0"
        >
          <div style="margin-right: 30px">
            <a href="https://uptrace.dev/" target="_blank" title="Distributed tracing and metrics">
              <img src="/uptrace/logo-large.svg" style="width: 175px; margin-bottom: 22px" />
            </a>
          </div>
          <div>
            <Newsletter />
          </div>
        </div>
      </template>
    </Layout>
  </div>
</template>

<script lang="ts">
import { ref, onMounted } from 'vue'
import Layout from '@vuepress/theme-default/lib/client/layouts/Layout.vue'

import Newsletter from '../../components/Newsletter.vue'

interface Link {
  text: string
  href: string
}

export default {
  components: {
    Layout,
    Newsletter,
  },

  setup() {
    const link = ref<Link>()

    onMounted(() => {
      link.value = randLink()

      setInterval(() => {
        link.value = randLink(link.value.href)
      }, 30000)
    })

    function randLink(currHref = '') {
      const links = [
        {
          text: 'Monitoring cache stats using OpenTelemetry Metrics',
          href: 'https://blog.uptrace.dev/posts/opentelemetry-metrics-cache-stats.html',
        },
        {
          text: 'Get latest updates right to your email',
          href: 'https://blog.uptrace.dev/pages/newsletter.html',
        },
        {
          text: 'Running PostgreSQL on ZFS and AWS',
          href: 'https://bun.uptrace.dev/postgres/tuning-zfs-aws-ebs.html',
        },
        {
          text: 'Soft deletes via Bun models or PostgreSQL views',
          href: 'https://bun.uptrace.dev/guide/soft-deletes.html',
        },
      ]

      for (let i = 0; i < 100; i++) {
        const link = links[Math.floor(Math.random() * links.length)]
        if (link.href !== currHref) {
          return link
        }
      }

      return links[0]
    }

    return { link }
  },
}
</script>

